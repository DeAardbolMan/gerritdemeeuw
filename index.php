			<p>Heel leuk, dat leven in Rotterdam, maar een meeuw, die moet de wereld zien. Dus ik ging op jonge leeftijd al op pad met als doel om zo veel mogelijk van deze ge-wel-dig-ge aardkloot te ontdekken. 
			Natuurlijk zijn die gekke aardbewoners al bijna overal geweest, maar het is toch veel mooier om zelf alles gezien te hebben?! Het begon allemaal in Rotterdam, nadat ik groot genoeg was 
			om te vliegen ging ik op pad. Mijn eerste bestemming was Groningen, mooi om te zien hoe ver dat volk nog achter loopt. Van Groningen ging ik naar Antwerpen, een prachtige stad, maar dat volk wat daar woont is volgens mij
			niet het slimste volk wat er bestaat. Er liepen twee kinderen over straat met een bak frietjes, ja zo noemen ze die dingen daar, en toen liet een van die kinderen heel dat bakje op straat donderen.. hmm, geluk voor mij. 
